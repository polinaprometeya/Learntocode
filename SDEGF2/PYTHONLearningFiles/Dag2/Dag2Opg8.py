from random import randint
Numb = randint(1,6)
print ("Random dice cast produced this number", Numb)

if (Numb == 1):
    print("Or as a Roman number - I")
elif (Numb == 2):
    print("Or as a Roman number - II")
elif (Numb == 3):
    print("Or as a Roman number - III")
elif (Numb == 4):
    print("Or as a Roman number - IV")
elif (Numb == 5):
    print("Or as a Roman number - V")
else:
    print("Or as a Roman number - VI")
